"""Centralized error codes for Jac compiler."""

from enum import Enum


class JacErrorCode(Enum):
    """Centralized error codes for the Jac compiler and runtime.

    Error codes are organized by category with numeric ranges:
    - E0000-E0999: General/Syntax errors
    - E1000-E1999: Type system errors
    - E2000-E2999: Semantic analysis errors
    - E3000-E3999: Code generation errors
    - E4000-E4999: Internal compiler errors (ICE)
    - W0000-W0999: Warnings
    """

    # General/Syntax Errors (E0000-E0999)
    SYNTAX_ERROR = "E0001"
    UNEXPECTED_TOKEN = "E0002"
    UNEXPECTED_EOF = "E0003"
    INVALID_CHARACTER = "E0004"
    MISSING_COLON = "E0005"
    MISSING_SEMICOLON = "E0006"
    MISSING_CLOSING_BRACE = "E0007"
    MISSING_CLOSING_PAREN = "E0008"
    MISSING_CLOSING_BRACKET = "E0009"
    INVALID_INDENTATION = "E0010"
    MISSING_TOKEN = "E0011"
    INCOMPLETE_MEMBER_ACCESS = "E0012"
    INVALID_PARSE_TREE = "E0013"
    INVALID_ASYNC_ARCHETYPE = "E0014"
    INVALID_FUNCTION_PARAMETERS = "E0015"
    INVALID_FUNCTION_PARAMETERS_ORDER = "E0016"

    # Symbol Table Errors (E1000-E1999)
    SYMBOL_NOT_FOUND = "E1001"
    DUPLICATE_SYMBOL_DECL = "E1002"
    SYMBOL_REDECLARED = "E1003"
    INVALID_SYMBOL_NAME = "E1004"
    PRIVATE_SYMBOL_ACCESS = "E1005"
    PROTECTED_SYMBOL_ACCESS = "E1006"

    # Type System Errors (E2000-E2999)
    TYPE_MISMATCH = "E2001"
    TYPE_ASSIGNMENT_ERROR = "E2002"
    TYPE_NOT_FOUND = "E2003"
    INCOMPATIBLE_TYPES = "E2004"
    MISSING_TYPE_ANNOTATION = "E2005"
    INVALID_TYPE_ARGUMENT = "E2006"
    GENERIC_TYPE_ERROR = "E2007"
    TYPE_INFERENCE_FAILED = "E2008"
    TYPE_ASSIGNMENT_TO_PARAMETER = "E2009"
    MISSING_FUNCTION_PARAMETERS = "E2010"

    # Semantic Analysis Errors (E3000-E3999)
    INVALID_TARGET_CONTEXT = "E3001"
    MISSING_ATTRIBUTE = "E3002"
    INVALID_OPERATION = "E3003"
    INVALID_IMPORT = "E3004"
    MODULE_NOT_FOUND = "E3005"
    CIRCULAR_IMPORT = "E3006"
    INVALID_STRING_IMPORT = "E3007"
    MISSING_MODULE_IN_PROGRAM = "E3008"
    INVALID_ASSIGNMENT_TARGET = "E3009"
    INVALID_NAMED_TARGET = "E3010"
    INVALID_FOR_LOOP_TARGET = "E3011"
    INVALID_FOR_EXPR_TARGET = "E3012"
    EMPTY_MODULE_PATH = "E3013"
    LENGTH_MISMATCH_ASYNC_FOR = "E3014"
    LENGTH_MISMATCH_IMPORT = "E3015"
    ABSTRACT_ABILITY_HAS_BODY = "E3016"
    ABSTRACT_ABILITY_HAS_DEFINITION = "E3017"
    PARAMETER_COUNT_MISMATCH = "E3018"
    NON_DEFAULT_AFTER_DEFAULT = "E3019"
    MISSING_POSTINIT_METHOD = "E3020"
    ARCHETYPE_NO_BODY = "E3021"
    ABILITY_NO_BODY = "E3022"
    INVALID_PIPE_TARGET = "E3023"
    UNSUPPORTED_BINARY_OPERATOR = "E3024"
    INVALID_ATTRIBUTE_ACCESS = "E3025"

    # Code Generation Errors (E4000-E4999)
    CODEGEN_FAILED = "E4001"
    BYTECODE_GEN_FAILED = "E4002"
    AST_NOT_FOUND = "E4003"
    UNSUPPORTED_FEATURE = "E4004"
    FORMATTING_ERROR = "E4005"

    # Internal Compiler Errors (E5000-E5999)
    ICE_UNEXPECTED_NODE = "E5001"
    ICE_MISSING_SYMBOL_INFO = "E5002"
    ICE_PASS_ERROR = "E5003"
    ICE_UNKNOWN_ERROR = "E5004"

    # Warnings (W0000-W0999)
    UNUSED_VARIABLE = "W0001"
    UNUSED_IMPORT = "W0002"
    POTENTIAL_NULL_DEREF = "W0003"
    DEPRECATED_FEATURE = "W0004"
    ATTRIBUTE_ERROR = "W0005"
    EXPERIMENTAL_FEATURE = "W0006"
    MODULE_NOT_FOUND_WARNING = "W0007"

    def __str__(self) -> str:
        """Return the error code as a string."""
        return self.value

    def is_warning(self) -> bool:
        """Check if this error code represents a warning."""
        return self.value.startswith("W")

    def is_error(self) -> bool:
        """Check if this error code represents an error."""
        return self.value.startswith("E")
